# Страница с формулами по алгебре
@app.route('/algebra')
def algebra():
    formulas = {
        'Уравнения (найти x)': [
            'x + a = b  →  x = b - a',
            'Пример: x + 5 = 12  →  x = 12 - 5 = 7',
            'x - a = b  →  x = b + a',
            'Пример: x - 3 = 8  →  x = 8 + 3 = 11',
            'a × x = b  →  x = b ÷ a',
            'Пример: 4 × x = 20  →  x = 20 ÷ 4 = 5'
        ],
        'Формулы сокращённого умножения': [
            '(a + b)² = a² + 2ab + b²',
            'Пример: (3 + 4)² = 3² + 2×3×4 + 4² = 9 + 24 + 16 = 49',
            '(a - b)² = a² - 2ab + b²',
            'Пример: (5 - 2)² = 5² - 2×5×2 + 2² = 25 - 20 + 4 = 9'
        ],
        'Проценты': [
            '1% = 1/100 = 0.01',
            'Найти процент от числа: число × процент ÷ 100',
            'Пример: 15% от 200 = 200 × 15 ÷ 100 = 30',
            'Найти число по проценту: часть ÷ процент × 100',
            'Пример: 25 это 20% от какого числа? 25 ÷ 20 × 100 = 125'
        ]
    }
    return render_template('algebra.html', formulas=formulas)

